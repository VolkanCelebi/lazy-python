#Örneğin Aç-Kapa fonksiyonu yazıyor bizim junior python cılar
def ac_kapa(self):
    if self.toggled == false:
        self.toggled = true
    else:
        self.toggled = false

#Şimdi üşengeç python cıya bakalım    
def ac_kapa_usengec(self):
    self.toggled = not self.toggled



